
public class Guns
	{
		private String Name, set;
		private int Ammunation, Damage;
		public void Gun (String col, String cl, int a, int buns)
			{
				Name = col;
				set = cl;
				Ammunation = a;
				Damage = buns;
			}
		public String getName()
			{
				return Name;
			}
		public void setName(String name)
			{
				Name = name;
			}
		public String getset()
			{
				return set;
			}
		public void setset(String class1)
			{
				set = class1;
			}
		public int getAmmunation()
			{
				return Ammunation;
			}
		public void setAmmunation(int ammunation)
			{
				Ammunation = ammunation;
			}
		public int getDamage()
			{
				return Damage;
			}
		public void setDamage(int damage)
			{
				Damage = damage;
			}
	}
